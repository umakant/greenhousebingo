// Idempotently creates project_staff_sow table for employee Scope of Work documents.

const path = require("path");
require("dotenv").config({ path: path.join(__dirname, "..", ".env.local") });
require("dotenv").config({ path: path.join(__dirname, "..", ".env") });

const { PrismaClient } = require("@prisma/client");

const prisma = new PrismaClient({ log: ["error"] });

const STATEMENTS = [
  `CREATE TABLE IF NOT EXISTS project_staff_sow (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    project_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    partner_name VARCHAR(255) NULL,
    locations TEXT NULL,
    schedule_details TEXT NULL,
    total_rate VARCHAR(255) NULL,
    per_diem TEXT NULL,
    dress_code TEXT NULL,
    policies TEXT NULL,
    travel_notes TEXT NULL,
    payroll_notes TEXT NULL,
    sign_by_date DATE NULL,
    status VARCHAR(32) NOT NULL DEFAULT 'draft',
    signed_at TIMESTAMP NULL,
    signed_file_path TEXT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NULL
  );`,
  `CREATE UNIQUE INDEX IF NOT EXISTS project_staff_sow_project_user_key ON project_staff_sow(project_id, user_id);`,
  `CREATE INDEX IF NOT EXISTS project_staff_sow_project_id_idx ON project_staff_sow(project_id);`,
  `ALTER TABLE project_staff_sow ADD COLUMN IF NOT EXISTS form_data JSONB NULL;`,
];

async function main() {
  for (const sql of STATEMENTS) {
    await prisma.$executeRawUnsafe(sql);
  }
  console.log("project_staff_sow schema ensured.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
