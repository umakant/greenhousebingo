// Idempotently creates project_employee_pay_rates for per-project employee pay.

const path = require("path");
require("dotenv").config({ path: path.join(__dirname, "..", ".env.local") });
require("dotenv").config({ path: path.join(__dirname, "..", ".env") });

const { PrismaClient } = require("@prisma/client");
const prisma = new PrismaClient({ log: ["error"] });

const STATEMENTS = [
  `CREATE TABLE IF NOT EXISTS project_employee_pay_rates (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    project_id BIGINT NOT NULL,
    user_id BIGINT NOT NULL,
    role VARCHAR(32) NULL,
    rate_type VARCHAR(32) NOT NULL DEFAULT 'per_day',
    pay_rate DECIMAL(10, 2) NOT NULL,
    half_day_rate DECIMAL(10, 2) NULL,
    notes TEXT NULL,
    created_by BIGINT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMP NULL
  );`,
  `CREATE UNIQUE INDEX IF NOT EXISTS project_employee_pay_rates_project_user_key ON project_employee_pay_rates(project_id, user_id);`,
  `CREATE INDEX IF NOT EXISTS project_employee_pay_rates_project_id_idx ON project_employee_pay_rates(project_id);`,
  `CREATE INDEX IF NOT EXISTS project_employee_pay_rates_user_id_idx ON project_employee_pay_rates(user_id);`,
];

async function main() {
  for (const sql of STATEMENTS) {
    await prisma.$executeRawUnsafe(sql);
  }
  console.log("project_employee_pay_rates schema ensured.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
