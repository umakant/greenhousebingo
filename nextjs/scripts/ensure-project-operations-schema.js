async function main() {
  console.log("Ensuring project operations schema…");
  for (const sql of STATEMENTS) {
    await prisma.$executeRawUnsafe(sql);
  }
  await prisma.$executeRawUnsafe(`
    UPDATE projects p
    SET lead_section_access = pla.section_access
    FROM project_lead_assignments pla
    WHERE pla.project_id = p.id
      AND p.lead_section_access IS NULL
      AND pla.section_access IS NOT NULL;
  `);
  console.log("Project operations schema OK.");
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
