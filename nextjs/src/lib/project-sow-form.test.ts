describe("buildWorkPeriodLabelFromDates", () => {
  it("rebuilds activation range labels with MM/DD/YYYY and day count", () => {
    expect(
      buildWorkPeriodLabelFromDates("2026-06-12", "2026-06-30", "06/12 – 06/29 Activation Days (20)"),
    ).toBe("06/12/2026 – 06/30/2026 Activation Days (19)");
  });

  it("keeps travel day wording for single-day travel periods", () => {
    expect(
      buildWorkPeriodLabelFromDates("2026-07-01", "2026-07-01", "06/30/2026 Travel Day"),
    ).toBe("07/01/2026 Travel Day");
  });
});

