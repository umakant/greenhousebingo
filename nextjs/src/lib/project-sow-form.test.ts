describe("buildDefaultSowFormData", () => {
  it("prefills vendor, venue, and work periods from project + assignments", () => {
    const form = buildDefaultSowFormData(
      projectMeta,
      { name: "Lynn Nicely", email: "lynn@example.com" },
      assignments,
    );

    expect(form.vendor_company_name).toBe("Crimson Consulting");
    expect(form.client_contact_name).toBe("Lynn Nicely");
    expect(form.event_name).toBe("Super Bowl");
    expect(form.primary_venue).toBe("Allegiant Stadium");
    expect(form.city).toBe("Las Vegas");
    expect(form.work_periods.length).toBe(1);
    expect(form.work_periods[0]?.start_date).toBe("2026-06-16");
    expect(form.work_periods[0]?.label).toContain("Activation Days (3)");
    expect(form.per_diem).toContain("$85");
  });
});

describe("formDataToSowRecord", () => {
  it("builds locations and schedule text for PDF output", () => {
    const defaults = buildDefaultSowFormData(
      projectMeta,
      { name: "Lynn Nicely", email: "lynn@example.com" },
      assignments,
    );
    const form = mergeSowFormData(
      {
        client_company_name: "On Location",
        compensation_summary: "9 total days @ $5400",
        work_periods: [
          {
            id: "1",
            label: "4/13 – 4/20 Activation Days (8)",
            start_date: "2026-04-13",
            end_date: "2026-04-20",
            daily_rate: "600",
            rate_type: "per day",
          },
        ],
      },
      defaults,
    );

    const record = formDataToSowRecord(form);
    expect(record.partner_name).toBe("On Location");
    expect(record.locations).toContain("Allegiant Stadium");
    expect(record.schedule_details).toContain("Activation Days");
    expect(record.schedule_details).toContain("$600");
    expect(record.total_rate).toBe("9 total days @ $5400");
  });
});

describe("mergeSowWithDefaults", () => {
  it("keeps stored values over defaults", () => {
    const defaults = buildDefaultSowContent(projectMeta, "Lynn Nicely", assignments);
    const merged = mergeSowWithDefaults(
      { total_rate: "Custom rate", partner_name: "On Location" },
      defaults,
    );
    expect(merged.total_rate).toBe("Custom rate");
    expect(merged.partner_name).toBe("On Location");
    expect(merged.per_diem).toBe(defaults.per_diem);
  });
});

describe("mergeSowFormData", () => {
  it("fills empty stored location fields from project defaults", () => {
    const defaults = buildDefaultSowFormData(
      { ...projectMeta, address: "100 Wall Street", address_2: "Suite 2800" },
      { name: "Lynn Nicely", email: "lynn@example.com" },
      assignments,
    );
    const merged = mergeSowFormData(
      {
        primary_venue: "",
        venue_address: "",
        city: "",
        state: "",
        zip_code: "",
        timezone: "",
      },
      defaults,
    );

    expect(merged.primary_venue).toBe("Allegiant Stadium");
    expect(merged.venue_address).toBe("100 Wall Street, Suite 2800");
    expect(merged.city).toBe("Las Vegas");
    expect(merged.state).toBe("NV");
    expect(merged.zip_code).toBe("89118");
    expect(merged.timezone).toBe("(GMT-08:00) Pacific Time");
  });

  it("fills empty stored per diem and dress code from project defaults", () => {
    const defaults = buildDefaultSowFormData(
      {
        ...projectMeta,
        sow_per_diem: "On Location will provide meals where applicable.",
        sow_dress_code: "Dark suit and dress shoes.",
      },
      { name: "Lynn Nicely", email: "lynn@example.com" },
      assignments,
    );
    const merged = mergeSowFormData({ per_diem: "", dress_code: "" }, defaults);
    expect(merged.per_diem).toContain("On Location will provide meals");
    expect(merged.dress_code).toBe("Dark suit and dress shoes.");
  });
});

